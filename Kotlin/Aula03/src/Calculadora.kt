class Calculadora {
    companion object{
        fun somar(n1: Int, n2: Int): Int{
            return n1 + n2
        }

        fun subtrair(n1: Int, n2: Int): Int{
            return n1 - n2
        }

        fun dividir(n1: Int, n2: Int): Int{
            return n1 / n2
        }

        fun multiplicar(n1: Int, n2: Int): Int{
            return n1 * n2
        }
    }
}